# Get input from the user for the first number
num1_str = input("Enter the first number: ")

# Get input from the user for the second number
num2_str = input("Enter the second number: ")

# Convert the input strings to integers (or floats if decimal numbers are expected)
num1 = int(num1_str)
num2 = int(num2_str)

# Calculate the sum
sum_of_numbers = num1 + num2

# Display the result
print(f"The sum of {num1} and {num2} is: {sum_of_numbers}")